    <h1>Hysterectomy and Alzheimer’s Disease</h1>
     <p>Alzheimer's disease is the most common cause of dementia — a continuous decline in thinking, behavioural and social skills that affects a person's ability to function independently.
In 2020, COVID-19 contributed to a 17% increase in Alzheimer's and dementia deaths.

 </p>
    <p>Having hysterectomy at an early age or positive family history increases the odds of Alzheimer’s.
Hysterectomy and Alzheimer’s disease:  According to a study published in the journal Neurology, shorter reproductive life spans and hysterectomy increase a woman’s risk of developing some type of dementia, although the reasons for this are still unclear.


</p>
<p>What is clear is the statistics. Of the 15,754 women the study examined over a 21 year period, 42% of them developed dementia.
A 2019 systematic review and meta-analysis of women undergoing surgical menopause show a faster decline in various tests of memory and accumulation of Alzheimer’s neuropathology.
*Surgical menopause or hysterectomy ups the risk of decline in memory (dementia) and Alzheimer’s disease.

</p>



