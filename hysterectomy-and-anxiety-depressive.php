    <h3>Symptoms of depression:</h3>
    <ul>
      <li>• Persistent feelings of sadness
</li>
      <li>• Decreased energy or fatigue
</li>
      <li>• Difficulty concentrating, remembering, or making decisions
</li>
<li>• Lack of self-care</li>
<li>• Aches or pains, headaches, cramps, or digestive problems </li>
<li>• Routinely misplace possessions, often putting them in illogical locations</li>
<li>• Withdrawal from social activities</li>
<li>• Thoughts of death or suicide</li>
<li>• Lack of interest in things you once enjoyed</li>
<li>• Irritability</li>
<li>• Fatigue</li>
<li>• Feelings of hopelessness</li>
<li>• Worthlessness</li>
<li>• Pessimism accompanied by physical symptoms</li>
</ul>
     
   
    <h3>Symptoms of anxiety:</h3>
  
    <ul>
      <li>• Feeling nervous, restless or tense</li>
      <li>• Having a sense of impending danger, panic or doom</li>
      <li>• Having an increased heart rate</li>
      <li>• Breathing rapidly (hyperventilation)</li>
      <li>• Sweating</li>
      <li>• Trembling</li>
      <li>• Feeling weak or tired</li>
      <li>• Trouble concentrating or thinking about anything other than the present worry</li>
      <li>• Having trouble sleeping</li>
      <li>• Experiencing gastrointestinal (GI) problems</li>
     
      

    </ul>



    <h3>Other causes-</h3>
  
    <ul>
      <li>• Medical problems like - Heart disease, Diabetes, Thyroid problems, COPD and Asthma</li>
      <li>• Drug misuse or withdrawal</li>
      <li>• Withdrawal from alcohol, anti-anxiety medications (benzodiazepines) or other medications</li>
      <li>• Chronic pain </li>
      <li>• Irritable bowel syndrome</li>
      <li>• Personality-People with certain personality types are more prone to anxiety disorders than others are.</li>
      <li>• Trauma</li>
      <li>• Having blood relatives with an anxiety disorder</li>
     
    </ul>


    
    
