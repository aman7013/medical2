    <h1>Hysterectomy and Anxiety Depressive Disorder</h1>
     <p>Hysterectomy can also impact emotional health. It can come with a feeling of lingering sadness, worthlessness, social withdrawal and irritability etc. Collectively these symptoms are known as depression.
We know that shifting hormones play a role in depression and that otherwise also women are twice as likely to experience depression. It’s not emotional its biochemical impact.

 </p>
    <p><b>Anxiety disorder:</b> : Experiencing occasional anxiety is a normal part of life. However, people with anxiety disorders frequently have intense, excessive and persistent worry and fear about everyday situations. Often, anxiety disorders involve repeated episodes of sudden feelings of intense anxiety and fear or terror that reach a peak within minutes (panic attacks).


</p>
<p>Hysterectomy is a major procedure that can have a lasting impact on a woman's life.
Women who are predisposed to depression, anxiety, or hostility before an operation are at a much higher risk of suffering from the same psychological distress afterwards. 
Hysterectomy is associated with an increased risk of long-term mental health issues, especially depression and anxiety, according to a cohort study by Mayo Clinic researchers involving nearly 2,100 women.
One of the prevailing professional apprehensions in recent years has been the incidence of depression after a hysterectomy, underlining the argument that a medically successful operation does not essentially equate holistic success.


</p>

<p><b>Incidence/Prevalence:</b> One study performed showed that half of the women (50%) who underwent a hysterectomy suffered from anxiety-depressive disorders as a direct result of the operation, and a quarter required help from specialists.
Women under age of 35 who had a hysterectomy were at a 47 percent increased risk for depression and a 45 percent increased risk for anxiety. The reason for the operation — fibroids, menstrual disorders or uterine prolapse — did not affect the association.
</p>

