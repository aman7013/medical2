<p>According to a research, Hysterectomy is not a causal factor of constipation. However, constipation may develop after hysterectomy, especially in those women who have a larger uteral size and in whom the cervix is preserved. In about half of the women undergoing hysterectomy constipation had disappeared at three year after surgery. Especially those women who underwent hysterectomy for different reasons from menorrhagia, metrorrhagia, abdominal pain or dysmenorrhoea, the risk on persistence of constipation appeared to be present.</p>
 



