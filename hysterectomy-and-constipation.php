    <h1>Hysterectomy and Constipation</h1>
     <p>Constipation is a condition in which you may have fewer than three bowel movements per week, stools that are hard, dry, or lumpy, stools that are difficult or painful to pass, or a feeling that not all stool has passed.
 </p>
    <p><b>Cause:</b> Constipation is in part frequently associated with abdominal surgeries. A hysterectomy will bring on surgical menopause; the body has had no time to adjust to the sudden drop in estrogen levels. 
Declining levels of progesterone and estrogen can cause- 

</p>
<p><b>Stools to stay in the colon longer.</b> The longer the stool stays in the colon, the more they dry out, which leads to small, dry, hard stools that are difficult to pass.
A weakening pelvic floor can make it more difficult to fully pass stool,
 It also slows the digestion process, decreasing movement in the colon, which leads to constipation. Surgical menopause causes abrupt transition from Pitta phase of life to Vata phase. One of the effects of deranged Vata is dryness. It shows as dry eyes, mouth, nose, skin and even dry stools.
</p>

