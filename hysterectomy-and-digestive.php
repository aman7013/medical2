    <h1>Hysterectomy and Digestive Issues</h1>
     <p>Women passing through the menopausal transition due to surgery or naturally may encounter some form of upset in their oesophagus, stomach, or intestines that can provoke 
 </p>
 <ul>
  <li>• Bloating</li>
  <li>• Constipation</li>
  <li>• Diarrhoea</li>
  <li>• Acid reflux</li>
  <li>• Irritable bowel syndrome (IBS)</li>
 </ul>
    <p><b>Incidence/Prevalence:</b> These symptoms may occur immediately after surgery, but for some people, these symptoms can persist even after they’ve recovered from surgery.

</p>
<p><b>Hysterectomy and digestive symptoms: </b>Because a hysterectomy occurs close to the bowels, the surrounding nerves and pelvic floor could potentially become damaged during the procedure. In addition, removal of the uterus can cause other organs like the intestines and bladder to shift downwards. For this reason, changes in bowel habits are common after having a hysterectomy.
</p>

  </div>
 
  <div class="ayur_box_c2">
    <h3>1. Bloating-</h3>
    <ul>
      <li>• Your stomach may stick out</li>
      <li>• Feeling full and/or uncomfortable</li>
      <li>• The skin around your stomach may feel stretched and tight</li>
    </ul>
    <h3>2. Acid reflux-</h3>
    <ul>
      <li>• Heartburn, also called as acid indigestion</li>
      <li>• Burning chest pain which begins behind the breastbone and move upwards to the neck and throat.</li>
      <li>• Difficulty in swallowing (dysphagia)</li>
      <li>• Persistent dry cough</li>
      <li>• Hoarseness or sore throat</li>
      <li>• Regurgitation of food or sour liquid (acid reflux)</li>
      <li>• Sensation of a lump in your throat</li>
    </ul>


    <h3>3. Irritable bowel syndrome (IBS)</h3>
    <ul>
      <li>• If autonomic nerves are damaged during surgery, it may impact bowel movement.</li>
      <li>• Rectocele may cause difficulty with voiding</li>
      <li>• Use of iron, opioids and NSAIDS may cause bowel issues</li>
      <li>• Depression and anxiety may also cause bowel issues</li>
      <li>• Stress</li>
      <li>• A poor diet</li>
      <li>• Certain medications</li>
      <li>• Bad eating habits</li>
      <li>• Excessive consumption of nicotine or alcohol</li>
    </ul>
     

 



    
    
