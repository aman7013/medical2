    <h1>Hysterectomy and Fatigue</h1>
     <p>Fatigue is physical and/or mental exhaustion. It is characterized by a profound lack of energy, feelings of muscle weakness
 and slowed movements or reactions. Persistent fatigues can cause- lack of mental clarity (or feeling of mental "fuzziness"), difficulty concentrating, and in some cases, memory loss.

 </p>
    <p><b>Incidence/Prevalence:</b> A hysterectomy is a major operation, both physically and emotionally - 75% of patients report debilitating fatigue following surgery that often lasts longer than the physical postoperative pain.
</p>
<p><b>Hysterectomy and fatigue: </b> It is not uncommon for women to suffer from exhaustion after hysterectomy, especially in the first few weeks after surgery. Many women also continued to feel fatigued beyond 6 months.
As a matter of fact, it can take up to a year or longer before women fully recover emotionally and physically.
Many women experience chronic fatigue and loss of stamina since their hysterectomies even if they still have their ovaries. 

</p>



