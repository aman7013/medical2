    <h1>Hysterectomy & Hair Fall</h1>
     <p>Hair is an expression of us, our personality and our image. It is the crowning glory for women. Hair loss or thinning is a reality for a lot of ladies in menopausal years. Quite frequently, the volume and condition of the hair appears to worsen, with some women noticing that hair does not grow as much as previously. 
 </p>
    <p><b>Incidence and Prevalence:</b> Hair loss is a big issue in women who have had hysterectomy. Recent studies have reported that menopausal hair loss occurs in more than 40 % of cases. </p>
    <p>Hysterectomy and hair fall: A major surgery like hysterectomy induces menopause and often disrupts the growth cycle of hair. From growing stage hair go into resting stage and then fall off. Increased incidence of hair thinning and hair fall has been reported.</p>
