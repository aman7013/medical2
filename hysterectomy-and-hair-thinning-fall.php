    <h3>Symptoms:</h3>
    <p>Most women experience overall hair thinning rather than noticeable bald spots. 
Hair may also fall out in large tufts during brushing and showering.
</p>
    <p>Post hysterectomy, the body goes through numerous physical changes as it adapts to fluctuating hormone levels. The changes can be real fast and symptoms intense.Research suggests that hair loss is related to a lowered production of estrogen and progesterone. These hormones help hair grow faster and stay on the head for longer periods of time. When the levels of estrogen and progesterone drop, hair grows more slowly and becomes much thinner. This does not mean that only hormones alone are to blame, there are several other factors, although oestrogen helps to keep hair in the ‘growing phase’.</p>
    <p><b>Other factors that contribute to hair loss:</b></p>
    <ul>
      <li>Lack of nutrients</li>
      <li>Stress</li>
      <li>Thyroid dysfunction</li>
      <li>Anaemia</li>
      <li>Raised testosterone levels </li>
      <li>Skin disorders</li>
      <li>Cosmetic practices, e.g. use of straighteners, hair dryers and hair colours</li>

    </ul>
