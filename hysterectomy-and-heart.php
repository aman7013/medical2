    <h1>Hysterectomy and Heart Conditions</h1>
     <p>Hysterectomy (uterus removal surgery) raises the risk for many Heart diseases like Hyperlipidaemia, Hypertension, Coronary artery disease, Cardiac arrhythmias, Ischemic stroke and Heart failure etc.
 </p>
 <p><b>Research findings on Hysterectomy and heart diseases:</b></p>
    <p>1. Hysterectomy is associated with an increased risk of cardiovascular disease. A study conducted by Mayo Clinic found that women who had a hysterectomy before age 35 are at a particularly high risk. Specifically, their risk was 4.6-fold for congestive heart failure and 2.5-fold for coronary artery disease.</p>
    <p>2. In a study Trusted Source conducted in China, researchers reported that women who had hysterectomies with ovary and tube removal had an almost 20% higher risk of cardiovascular disease and ischemic stroke than women who did not have surgery. 
Women who had HA had 9% higher risk of CVD after HA and 19% higher risk of CVD after HBO.
*Hysterectomy alone (HA) –only uterus removal
 *Bilateral oophorectomy (HBO)- uterus and ovarian removal</p>
    
  </div>
 
  <div class="ayur_box_c2">
    

    <h3>Symptoms of Heart conditions for which the risk amplified after hysterectomy:</h3>
    <p><b>1. Hyperlipidaemia (High levels of fat):</b>
</p>
    <p>High levels of fat including cholesterol and triglycerides in the blood can go unnoticed for a long time but hysterectomy can hasten it up. When fat builds up in the arteries, which transport blood from the heart to other parts of the body, the arteries become narrow, reducing or blocking blood flow. It can cause other heart conditions like high B.P, CAD, and Vascular disease.</p>
    <p><b>Symptoms of these conditions can include:</b></p>
    <ul>
      <li>Shortness of breath</li>
      <li>Dizziness</li>
      <li>Chest pain</li>
      <li>Numbness or tingling</li>
      <li>Swelling, especially in the hands and feet</li>
      <li>Poor wound healing</li>
      <li>Skin problems or discoloration</li>
      <li>High blood pressure</li>
    </ul>


    <p><b>2. CAD (Coronary artery disease):</b>Symptoms of CAD are not noted until the artery is significantly damaged. Severe artery damage decreases blood flow to the heart, which presents as:</p>
    <ul>
      <li>Angina or chest pain that spreads across the chest</li>
      <li>Shortness of breath</li>
      <li>Heart attack (IHD) is experienced if the coronary artery is completely blocked</li>
    </ul>
    <p><b>  Other associated symptoms include:</b></p>
    <ul>
      <li>Nausea</li>
      <li>Sweating</li>
      <li>Fatigue</li>
      <li>Faster heartbeat</li>
      <li>Weakness or dizziness</li>
    </ul>
    <p><b>3. Cardiac arrhythmias:</b></p>
    <ul>
      <li>Shortness of breath, dry cough</li>
      <li>Swollen ankles, legs, and abdomen (body swelling), weight gain, increased frequency of urination at night</li>
      <li>Rapid or irregular heartbeats</li>
      <li>Weight gain</li>
      <li>Chest pain</li>
      <li>Fainting in severe cases</li>

    </ul>
    <p><b>4. Heart failure:</b></p>
    <ul>
      <li>Heart failure is a condition where the heart muscle doesn’t pump blood as well as it should. </li>
      <li>Heart failure may be symptomless or range from mild to severe.</li>
      <li>Shortness of breath with activity or when lying down.
        <li>Fatigue and weakness.</li>
        <li>Swelling in the legs, ankles, and feet.</li>
        <li>Rapid or irregular heartbeat.</li>
        <li>Reduced ability to exercise.</li>
        <li>Wheezing.</li>
        <li>A cough that doesn’t go away or a cough that brings up white or pink mucus with spots of blood.</li>
        <li>Swelling of the belly area.</li>
        <li>Very rapid weight gain from fluid build up.</li>
        <li>Nausea and lack of appetite.</li>
        <li>Difficulty concentrating or decreased alertness.</li>
        <li>Chest pain if heart failure is caused by a heart attack</li>
    </ul>
