    <h1>Hysterectomy and Migraine</h1>
     <p>Migraine is a primary headache disorder. Headache is a common symptom that affects quality of life in midlife women. Several types that have various causes are possible. Migraine and other severe headache syndromes are closely linked to hormones. This is why women going through menopause can often suffer from debilitating headaches. 
There are also women who have never had migraines before, who experience “menopause migraines” for the first time.


 </p>
    <p><b> Incidence/Prevalence:</b> Women are three times more likely to experience a migraine than men. 
Migraine prevalence during menopause ranges from 10% to 29%. An early questionnaire survey performed in women attending a menopause clinic found that 57% of women reported headache and 29% reported migraine in the 3 months preceding the visit.
The available data are relatively concordant in showing that spontaneous menopause is associated with a more evident improvement of migraine compared with induced menopause (removal of uterus with or without ovaries).
</p>
<p><b>Migraine worsened in 9% of women after spontaneous menopause and in 67% after surgical menopause.</b></p>


