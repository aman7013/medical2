    <h1>Hysterectomy and Osteoporosis</h1>
     <p>Bone is living tissue that is constantly being broken down and replaced. Osteoporosis occurs when the creation of new bone doesn't keep up with the loss of old bone.
Osteoporosis is a disease that weakens bones to the extent, where they break easily—most often, bones in the hip, backbone (spine), and wrist. Osteoporosis is called a “silent disease” because one may not notice any changes until a bone fracture/s happens.

 </p>
    <p><b>Incidence and Prevalence:</b> Very common (More than 1 crore cases per year in India). It is the most common bone disease. Approximately one in two women and one in eight men over age 50 will have an osteoporosis–related fracture in their lifetime, and these fractures account for approximately $14 billion in direct medical costs. 
Women have a higher risk for osteoporosis because they have smaller, thinner bones compared to men. Another factor is that menopause has a greater effect on bone health. Going through menopause can increase a woman’s risk of developing osteoporosis.

</p>
<p><b>Hysterectomy and Osteoporosis:</b> The occurrence of osteoporosis was increased in patients who had undergone hysterectomy compared with that in matched control subjects regardless of bilateral oophorectomy (removal of ovaries) status.</p>


