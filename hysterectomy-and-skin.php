    <h1>Hysterectomy and Skin</h1>
     <p>Some of the main skin problems many women encounter due to surgically induced menopause are:

 </p>
    <p><b>Dry Skin- </b>The skin starts to thin during menopause with the outer layer becoming less efficient at holding water. Many people report that their skin becomes dryer and thinner as they age.  This is sometimes referred to as “crepey” skin. Crepey skin is skin that has become thinner, drier, and less elastic—similar in look and feel to crepe paper. Crepey skin is a natural result of aging that can appear all over the body.

</p>
<p><b>Itchy Skin-</b> At its worst, when the skin dries out too much during the change, you can develop pruritus, a feeling of itchiness making you feel like you want to scratch and keep on scratching.This can develop in the arms and legs, back or chest.  </p>
<p><b>Wrinkles-</b> More skin cells die or atrophy due to declining hormones. This results in the appearance of the skin altering and wrinkles appearing.  
Sweating: Thermoregulatory sweating is a mechanism the body uses to control its internal temperature. During menopause, sweating often increases and is linked to hot flashes. 
</p>
<p><b>Acne:</b> Hormonal levels going haywire can lead to the unfortunate development of adult acne for many women, post hysterectomy.</p>

<p><b>Hair Growth:</b> There is no hard evidence to show why facial hair can crop up on women undergoing menopause. Since this might be due to changing hormonal levels.</p>
<p><b>Wound Healing:</b> Reduction in the efficiency of wound healing is experienced, particularly if there is an ulcer present.</p>
