    <h1>Hysterectomy and Tingling Extremities</h1>
     <p>This menopause symptom (formally called paraesthesia) is characterised by a feeling of numbness and pins and needles sensations in the arms, legs, hands, feet, fingers, and toes. Other than being unpleasant, fortunately most cases of tingling extremities during menopause and postmenopause do not indicate that anything more serious is going on. Some women may lose their balance when walking due to the extremities numbness and fingers can sometimes lose the ability to grip and do delicate movements efficiently. 
In other cases, a burning sensation can take over as nerves are pinched or joints compressed.

 </p>
 
    

