    <h3>Types:</h3>
    <ul>
      <li><b>Stress Incontinence-</b>
 The most common kind of bladder control problem in older women is stress   incontinence.   Weakened muscles can’t hold back urine when you cough, exercise, sneeze, laugh, or lift something heavy. The result can be a small leakage of urine or a complete loss of control. It is most common in menopausal age
</li>
      <li><b>Urge Incontinence-</b>

When bladder muscles squeeze incorrectly or lose the ability to relax, there may a constant urge to urinate, even when your bladder is empty. You may also experience leaking of urine or loss of control. This is sometimes called an “overactive bladder.”
</li>
      <li><b>Overflow incontinence</b>
  When your bladder does not empty fully, this type of UI can show as continuous   urine dribbling. You can have a weak urinary stream, feel like urinating at night (nocturia), and increased urinary hesitancy. This can be caused by under activity of the bladder muscle.
</li>
     
   
    <h3>Other causes:</h3>
   
    <ul>
      <li>• <b>Drinking Alcohol or Caffeine</b>-Beverages with alcohol or caffeine fill your bladder quickly, causing you to urinate more often.</li>
      <li>• <b>Infections-</b> Infections of your urinary tract or bladder may cause temporary UI. When the infection is cleared, your UI will likely resolve or improve.</li>
