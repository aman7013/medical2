    <h1>Hysterectomy and Urine leakage/Urinary incontinence</h1>
     <p><b>Urinary incontinence:</b> Urinary incontinence (UI) is also known as “loss of bladder control” or “involuntary urinary leakage.” Millions of women experience it, and the frequency of UI tends to increase as the age advances. The loss of control can be very minor as only a few drops of urine leak when you laugh, exercise, cough, or pick up heavy objects. Or there could be a sudden urge to urinate and be unable to keep it in before reaching the restroom, resulting in an accident.


 </p>
    <p><b> Hysterectomy and UI</b>: Hysterectomies can be effective in treating abnormal vaginal bleeding, endometriosis, fibroid tumors and other medical issues.  However, they can also cause incontinence, in which bladder leaks urine. Incontinence can be more than simply an annoyance; it can disrupt work, socializing, sex and exercise. 
Women who have undergone a hysterectomy have a 60% higher risk of developing urinary incontinence later in life compared to women who haven’t had one. Having a hysterectomy appears to increase a woman's risk of developing stress incontinence, according to the largest study ever to examine the issue.
The study showed that the women who had hysterectomies were more than twice as likely to have a later surgery to improve stress urinary incontinence.

</p>
<p><b>Cause: </b>When the uterus is removed, the muscles in the pelvis can lose strength and sag.  The same muscles support the bladder, and when they weaken, so does bladder control. Sometimes the ovaries are also removed during a hysterectomy, which makes your estrogen level drop since the ovaries are the main organs responsible for secreting the hormone. Because estrogen helps your pelvic floor remain strong and flexible, a lack of it can contribute to incontinence, particularly the type known as stress incontinence.</p>


  </div>
 
