    <h3>Symptoms:</h3>
    <ul>
      <li>• Increase in body weight
</li>
      <li>• Back ache and joint pains
</li>
      <li>• Changes in body shape
</li>
<li>• Shortness of breath</li>
<li>• Sleep apnoea</li>
<li>• Excessive sweating</li>
<li>• Sweating in skin folds</li>
</ul>
     
   
    <h3>Other causes:</h3>
   <p>How much one might gain and over how long a time period is highly individual. A lot depends on overall diet, activity level, and muscle tone—and not just your menopausal status. Besides hormonal shift and changeover to Vata state, other factors that cause weight gain are-</p>
    <ul>
      <li>• Advancing age</li>
      <li>• Faulty lifestyle </li>
      <li>• Genetic factors- If your parents or other close relatives carry extra weight around the abdomen, you're likely to do the same</li>
      <li>• Lack of exercise</li>
      <li>• Unhealthy eating pattern</li>
      <li>• Irregular sleeping pattern- Hot flashes, night sweats, and increased anxiety are just some of the things that can keep menopausal people up at night. Research shows that lack of sleep can contribute to weight gain.</li>
      <li>• Thyroid issues- Hypothyroidism often causes weight gain</li>
      <li>• Medications- antidepressant,  psychotropic agent and steroids for long term</li>
      <li>• Stress</li>
      

    </ul>

    <p><b>Risks associated with Menopause Weight Gain:</b> Menopause weight gain can have serious implications for health. Excess weight, especially around midsection, increases your risk of many issues.</p>
    <ul>
      <li><b>1. Metabolic syndrome</b> is characterized by high blood pressure, high blood sugar, abnormal cholesterol and lipid levels, and excess abdominal fat. People are three times more likely to be obese or have metabolic syndrome after menopause than before it.</li>
      <li><b>2. Diabetes-</b> Diabetes, a disease characterized by having high blood sugar, can wreak havoc on your body, upping your risk of heart disease, stroke, and kidney damage. Post-menopausal women who were metabolically unhealthy /obese had four times the risk of developing diabetes than metabolically healthy weight.</li>
      <li><b>3. Heart Disease</b>-Cardio vascular disease is the leading cause of death for women. Its risk increases after menopause, be it surgical or natural. But obesity is another risk factor that adds to it.</li>
     <li><b>4. High Blood Pressure-</b> Menopause brings about the increased risk of blood pressure in women, post menopause. Menopausal weight gain tends to settle as belly fat. The bigger is a woman’s waist circumference, the greater is the risk of high B.P.</li>
     <li><b>5. Various cancers-</b> Excess weight also increases your risk of various types of cancer, including breast, colon and endometrial cancers.</li>
     <li><b>6. Osteoarthritis-</b> Osteoarthritis seems to mostly affect the joints that bear the most weight, namely the knees and the hips, although other joints can be affected by the disease as well.
Research studies indicate that weight gain seems to play a major role in determining the onset and progression of this disease. Therefore, if a menopausal woman maintains her body weight, she may prevent the early onset of this degenerative and painful disease. If she already has it, she can slow down its progression through her body by losing excess weight.
</li>
    </ul>
