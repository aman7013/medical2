    <h1>Hysterectomy and weight gain</h1>
     <p>It is defined as significant increase in body fat percentage.
As women get older, they might notice that maintaining their usual weight becomes more difficult. Weight gain around the menopause transition is a common complaint. It tends to settle mostly in the abdominal region.  Hence, the term “menopause belly” has been coined.



 </p>
    <p><b>Incidence and Prevalence: </b>*In a study of 500 women in India, assessed on average 4.7 years post menopause, 78% were overweight or obese, and 68% had truncal obesity.
Though this data applies to a small group, yet it points to the propensity of weight gain in menopausal stage.


</p>
<p><b>Hysterectomy and Weight gain:</b> The mean BMI (Body mass index) of women undergoing hysterectomy was significantly high which is consistent with obesity being a risk factor for uterine fibroids, the most common indication for hysterectomy among premenopausal women.
At the same time, weight gain is one adverse outcome of hysterectomy. Data from a prospective cohort study suggest that premenopausal women undergoing hysterectomy without bilateral oophorectomy are at increased risk for weight gain compared with women of similar age with intact uteri and ovaries. 
</p>


