<p>1. *According to a study published in Lancet, fifty-six women who have had hysterectomy done in the previous five years and fifty-six women who had had other operations answered a specially designed questionnaire. The results showed that hysterectomy is more commonly followed by several distressing symptoms than other operations. 
70% of the hysterectomy patients had postoperative depression and an almost equal number had hot flushes and urinary symptoms, and experienced extreme tiredness postoperatively. 
About 50% the hysterectomy patients had headaches, dizziness, or insomnia postoperatively, whereas all these symptoms occurred much less often in the women who had had operations other than hysterectomy.
Hysterectomy is often necessary, but comes with an assortment of risks and potential complications. Most hysterectomies are performed in premenopausal adult women to treat benign conditions. This matters because new research shows that the procedure could raise the odds of having other health problems later in life. Risks were even greater for women who have the surgery before age 35.
It has been known for some time that as soon as ovaries are removed, (also called oophorectomy) women go into menopause, which raises risk for many health conditions:
Even in cases where the ovaries have been kept intact, the loss of ovarian functions may happen over a period of time. Uterus and the ovaries share blood supply, so it is inevitable that once the uterus is removed the ovaries stop functioning to their fullest capacity.</p>
<p>2. In a study of 4,188 women, those who had hysterectomy without ovarian removal were more likely to have:
 High cholesterol, High blood pressure, Irregular heartbeat
 Obesity- it impedes the agility of women and their capacity to work. 
 Diabetes- Increased risk of diabetes has also been found to be linked to hysterectomy. 
 Alzheimer’s disease – Surgical menopause increases the odds of having Alzheimer’s disease
 Loss of height: Shocking as it may seem, but there is a great chance of reduction in your height after hysterectomy. Bone problems resulting from menopause cause the space between two vertebrae to reduce. This problem also causes nerves to jam between the bones, giving rise to issues like frozen shoulder, sciatica etc.
Bladder problems increase: Since the uterus provides support to the bladder and the bowels, urinary incontinence is seen among women who undergo hysterectomy. Even sneezing and laughing may cause loss of bladder control in such cases.</p>
<p>3. A study conducted by the Maharashtra Legislative Council revealed that out of the 13,861 women in Beed who had undergone the operation, more than 45 percent of them experienced aching joints, back pain, depression, insomnia etc. Other studies suggest that post-operation, side effects like ovarian reserve reduction, increased incidence of heart disease, and early onset of osteoporosis, amongst several others, can be witnessed. Considerable weight gain after the surgery is also common, which impedes the agility of women and their capacity to work. Increased risk of diabetes has also been found to be linked to hysterectomy.
