    <h1>Hysterectomy and its Silent Cost</h1>
   <p>Evidence based Information regarding the adverse effects associated with hysterectomy:</p>
   <p>Hysterectomy is a drastic procedure to remove the uterus; it becomes essential in some cases. Recovery after hysterectomy can be prolonged and challenging. 
One of the prevailing professional apprehensions in recent years has been the increase in incidence of many health issues, post hysterectomy, like- 
Heart disease, Dementia, Osteoporosis, Insomnia, Joint pains, Migraine, Mood swings, decline in cognitive function, Urinary incontinence, Weight gain, Increased lipids, Diabetes, Alzheimer’s disease etc. underlining the argument that a medically successful operation does not essentially equate holistic success.
Prevalence: In more economically developed countries, about 40% of women will have hysterectomy by the time they are 60 years old.
Women aged 40–49 years had a high prevalence of hysterectomy (9.3%) in India. That is almost 1 in every 10 women. It is done for other age groups as well but its data is not available at the moment. Seeing the population of India it is a huge number.</p>

