      <p> <b>Pelvic floor health: </b> Pelvic floor is a group of muscles found in the base of your pelvis (the bottom of torso).These muscles support pelvic organs like urinary bladder, uterus and urinary bladder. They have a role in maintaining urinary, bowel and sexual functions etc. Some yoga poses like bridge pose and the cat cow pose can help strengthen the pelvic floor muscles. This can be particularly beneficial after hysterectomy because during hysterectomy the surgeon may need to cut some of the pelvic floor muscles to access and remove uterus. This can very much impact the strength of pelvic floor muscles. Yoga poses may improve pelvic tone and prevent issues incontinence or prolapse.</p>
